# implicit type casting or type conversion - done automatically by python
print(6 / 3) #prints 2.0. Which is a float data type
print(9 // 3) # It does 9/3 first, then removes all the decimal places. It prints 3, which is a float
print(5 // 3) #prints 1 - // Is for end results with no decimal places.

print(2**3) #It gives access to an exponential, when you want to raise a number to a power

